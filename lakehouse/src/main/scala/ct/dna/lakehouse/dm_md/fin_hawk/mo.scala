case class DmMo(
    _mk_system: String,
    _mk_instance: String,
    @PK key_column: String,
    matnr: String,
    mtart: String,
    matkl: String,
    lvorm: String,
    wgbez: String,
    mtbez: String,
    has_hscode: Boolean,
    number_of_distinct_hscodes: Long
) extends Entity

object mo extends TableSpec[DmMo] with Updated.ByOneTransaction {

  override def sourceTableSpecs: Seq[TableSpec[Entity]] =
    Seq(dm_mdm, dm_marc, dm_t023t, dm_t134t)

  override def executeTransaction(
      table: Table,
      changeFeeds: Map[TableSpec[Entity], ChangeFeed]
  ): Result = {

    val feeds = sourceTableSpecs.map(s => s -> changeFeeds(s))

    // Nothing changed in any source → skip the run entirely.
    if (feeds.forall { case (_, f) => f.isUnchanged }) return Result.NoChanges

    val maraDf = changeFeeds(dm_mdm).toDF().alias("mara")
    // marc and t023t are dimensions relative to mdm here — broadcast to keep the
    // join map-side and let the per-key aggregate run on a single shuffle stage.
    val marcDf = broadcast(changeFeeds(dm_marc).toDF()).alias("marc")
    val t023tDf = broadcast(changeFeeds(dm_t023t).toDF()).alias("t023t")
    val t134tDf = broadcast(changeFeeds(dm_t134t).toDF()).alias("t134t")

    val joined = maraDf
      .join(
        marcDf,
        col("mara._mk_system") === col("marc._mk_system") &&
          col("mara._mk_instance") === col("marc._mk_instance") &&
          col("mara.matnr") === col("marc.matnr"),
        "left"
      )
      .join(
        t023tDf,
        col("mara._mk_system") === col("t023t._mk_system") &&
          col("mara._mk_instance") === col("t023t._mk_instance") &&
          col("mara.matkl") === col("t023t.matkl"),
        "left"
      )
      .join(
        t134tDf,
        col("mara._mk_system") === col("t134t._mk_system") &&
          col("mara._mk_instance") === col("t134t._mk_instance") &&
          col("mara.mtart") === col("t134t.mtart"),
        "left"
      )

    val aggregated = joined
      .groupBy(
        col("mara._mk_system"),
        col("mara._mk_instance"),
        col("mara.matnr"),
        col("mara.mtart"),
        col("mara.matkl"),
        col("mara.lvorm"),
        col("t023t.wgbez"),
        col("t134t.mtbez")
      )
      .agg(
        sum(when(col("marc.stawn") =!= "", 1).otherwise(0)).as("hscode_count"),
        countDistinct(when(col("marc.stawn") =!= "", col("marc.stawn"))).as("distinct_hscode_count")
      )
      .withColumn("has_hscode", col("hscode_count") > 0)
      .withColumn(
        "number_of_distinct_hscodes",
        when(col("hscode_count") === 0, lit(0L)).otherwise(col("distinct_hscode_count"))
      )
      .withColumn(
        "key_column",
        concat(col("mara._mk_system"), col("mara._mk_instance"), lit("_"), col("mara.matnr"))
      )

    val result = aggregated.select(
      col("mara._mk_system").as("_mk_system"),
      col("mara._mk_instance").as("_mk_instance"),
      col("key_column"),
      col("mara.matnr").as("matnr"),
      col("mara.mtart").as("mtart"),
      col("mara.matkl").as("matkl"),
      col("mara.lvorm").as("lvorm"),
      col("t023t.wgbez").as("wgbez"),
      col("t134t.mtbez").as("mtbez"),
      col("has_hscode"),
      col("number_of_distinct_hscodes")
    )

    table.overwriteByKeys(result)
  }

  override def validate(): Unit = {
    super.validate()
    require(
      sourceTableSpecs.toSet == Set(dm_mdm, dm_marc, dm_t023t, dm_t134t),
      s"mo sourceTableSpecs unexpected: $sourceTableSpecs"
    )
  }
}

