add_sheet(wb, "tcurf", TCURF_NAMES, build_tcurf_sheet(PHASE1_CURRENCIES + PHASE2_CURRENCIES))

# Target sheets
add_sheet(wb, "target_phase0", TGT_NAMES, [])
add_sheet(wb, "target_phase1", TGT_NAMES, build_target_rows(PHASE1_CURRENCIES))
add_sheet(wb, "target_phase2", TGT_NAMES, build_target_rows(PHASE2_CURRENCIES))


