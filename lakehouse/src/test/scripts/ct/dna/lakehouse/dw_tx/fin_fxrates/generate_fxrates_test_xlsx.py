"""Regenerate FxRatesTest.xlsx for ct.dna.lakehouse.dw_tx.fin_fxrates.FxRatesTest.

Run via:  python3 lakehouse/src/test/scripts/ct/dna/lakehouse/dw_tx/fin_fxrates/generate_fxrates_test_xlsx.py

------------------------------------------------------------------------------
Test design — end-to-end MERGE lifecycle for the fxrates DW transform.
------------------------------------------------------------------------------

The fxrates transform:
  1. Reads TCURR + TCURF from ct_gbl_ghp sources + an archive UC table
  2. Joins TCURR with TCURF (on mandt/kurst/fcurr/tcurr, gdatu >= condition)
  3. Computes EUR base rates, inverse rates, cross rates, identity rates
  4. MERGEs into target (update existing, insert new, delete removed)

Because each run recomputes ALL rates from scratch (full-load-on-change),
the test phases are:
  Phase 0 — empty TCURR/TCURF sources → empty target
  Phase 1 — load TCURR/TCURF with 2 currencies (USD, INR) → 9 target rows
  Phase 2 — replace TCURR with different currencies (USD, GBP) → 9 target rows
             (INR rows deleted, GBP rows inserted, USD rate unchanged)

Uses exact-fraction ukurs values to avoid floating-point comparison issues:
  USD: ukurs=-2.0 → rate=1/2=0.5    (exact)
  INR: ukurs=-4.0 → rate=1/4=0.25   (exact)
  GBP: ukurs=-0.5 → rate=1/0.5=2.0  (exact)

SAP inverted date: gdatu = 99999999 - yyyyMMdd
  gdatu=79739898 → 99999999-79739898=20260101 → rate_date=2026-01-01

The xlsx is regenerated by this script; expected target rows match the
production transform's arithmetic exactly.
"""
