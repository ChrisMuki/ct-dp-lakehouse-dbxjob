# ---------------------------------------------------------------------------
# Rate computation (mirrors Spark logic exactly)
# ---------------------------------------------------------------------------
def compute_rate(ukurs, ffact=1, tfact=1):
    """Compute final_rate exactly as the Spark transform does.

    For negative ukurs: rate = (tfact/ffact) * (1/|ukurs|)
    For positive ukurs: rate = (tfact/ffact) * ukurs
    """
    uk = Decimal(str(ukurs))
    ff = Decimal(str(ffact))
    tf = Decimal(str(tfact))

    if uk < 0:
        adjusted = Decimal(1) / abs(uk)
    else:
        adjusted = uk

    return float((tf / ff) * adjusted)


def compute_all_rates(currencies):
    """Compute all output rates for a set of currencies.

    currencies: list of (fcurr, ukurs, ffact, tfact)
    Returns: sorted list of (rate_date, fcurr, tcurr, kurst, final_rate) tuples
    """
    # EUR base rates
    eur_rates = {}
    for fcurr, ukurs, ffact, tfact in currencies:
        rate = compute_rate(ukurs, ffact, tfact)
        eur_rates[fcurr] = rate

    rows = []

    # EUR base rates: fcurr → EUR
    for fcurr, rate in eur_rates.items():
        rows.append((RATE_DATE, fcurr, TARGET_CURRENCY, KURST, rate))

    # EUR inverse rates: EUR → fcurr
    for fcurr, rate in eur_rates.items():
        inv = 1.0 / rate
        rows.append((RATE_DATE, TARGET_CURRENCY, fcurr, KURST, inv))

    # Cross rates: fcurr_a → fcurr_b = rate_a / rate_b
    fcurrs = list(eur_rates.keys())
    for a in fcurrs:
        for b in fcurrs:
            if a != b:
                cross = eur_rates[a] / eur_rates[b]
                rows.append((RATE_DATE, a, b, KURST, cross))

    # Identity rates: fcurr → fcurr = 1.0
    for fcurr in fcurrs:
        rows.append((RATE_DATE, fcurr, fcurr, KURST, 1.0))

    # Target identity: EUR → EUR = 1.0
    rows.append((RATE_DATE, TARGET_CURRENCY, TARGET_CURRENCY, KURST, 1.0))

    # Deduplicate by PK (rate_date, fcurr, tcurr, kurst)
    seen = {}
    for row in rows:
        key = (row[0], row[1], row[2], row[3])
        if key not in seen:
            seen[key] = row

    # Sort for deterministic output
    return sorted(seen.values(), key=lambda r: (r[0], r[1], r[2], r[3]))


