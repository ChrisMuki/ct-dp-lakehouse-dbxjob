package ct.dna.lakehouse.dm_md.fin_hawk

import ct.dna.lakehouse.core.testutils.TestDataManager
import ct.dna.lakehouse.core.testutils.TestForTable
import ct.dna.lakehouse.sr.ct_gbl_e32
import ct.dna.lakehouse.sr.ct_gbl_epp
import ct.dna.utils.ResourceLoader
import org.apache.spark.sql.SparkSession

/** End-to-end test for `ct.dna.lakehouse.dm_md.fin_hawk.makt`.
  *
  * Drives the full production code path (`UpdatedTableProcessor.update(makt)` → `makt.executeTransaction`) against real Delta tables on a local ephemeral Spark
  * / Hive metastore. Each phase mutates source tables via [[TestDataManager.syncToSource]], runs the update, and asserts the resulting target contents against
  * an Excel sheet via [[TestDataManager.diffWithTarget]].
  *
  * Coverage: the fixture enumerates the full cross-product of per-language transition cases. For each language X ∈ {D, E} there are 12 transitions a single
  * source row can undergo from one snapshot ("pre") to the next ("post"):
  *
  *   - Case 1: value v1 → other value v2
  *   - Case 2: value v1 → NULL (maktx becomes NULL in an existing row)
  *   - Case 3: value v1 → same value v1, but another column (`_mk_created_at`) differs ⇒ CDF update with no maktx change
  *   - Case 4: value v1 → row bit-identical (no CDF event)
  *   - Case 5: value v1 → row deleted
  *   - Case 6: NULL → value v
  *   - Case 7: NULL → NULL, another column differs
  *   - Case 8: NULL → row bit-identical (no CDF event)
  *   - Case 9: NULL → row deleted
  *   - Case 10: no row → row with value
  *   - Case 11: no row → no row (no event, no row)
  *   - Case 12: no row → row with NULL maktx
  *
  * The fixture instantiates the full 12 × 12 matrix as `matnr = D{i:02}E{j:02}` (i = D case, j = E case). The combination (11, 11) is omitted because the matnr
  * would not exist at all — leaving 143 matnrs. `_lh_ingest_warning` carries a per-row label `"D=cN;E=cM"` so a diff immediately points at the failing
  * combination.
  *
  * E32 and EPP source rows are byte-identical except for `_mk_system`. `_mk_org = "CT"`, `_mk_site = "GBL"` everywhere. The xlsx is regenerated by
  * `lakehouse/src/test/scripts/generate_makt_test_xlsx.py`, which also computes the expected target rows by simulating the production merge.
  *
  * Phases:
  *
  *   1. Phase 0 — all 13 source tables and the target table exist but are empty. Running the update must leave the target empty.
  *
  *   1. Phase 1 — `ct_gbl_e32.makt` and `ct_gbl_epp.makt` receive their "pre" snapshots. The framework still treats these feeds as CDF (knownCommit is the
  *      create-time commit from Phase 0), but every key is an `insert`. Both halves of the target (E32 and EPP) are populated.
  *
  *   1. Phase 2 — only `ct_gbl_e32.makt` is `syncToSource`-aligned to its "post" snapshot. The pre→post diff drives a per-row CDF stream (insert / update /
  *      delete) covering every transition. EPP rows must remain bit-identical to phase 1.
  *
  *   1. Phase 3 — `ct_gbl_epp.makt` is dropped and re-created, then loaded with its "post" snapshot. Because the previously-recorded known commit no longer
  *      exists, `ChangeFeedImpl.isSnapshot` returns true and `pivotByLanguage` forces `_changed_x = true` for every language. This exercises the snapshot
  *      branches: rows present in the snapshot overwrite the target (a missing language clears the corresponding `_maktx_x`), rows absent from the snapshot are
  *      deleted via `whenNotMatchedBySource` (gated on `_mk_system ∈ snapshotSystems`), and rows under non-snapshot systems (E32) must stay untouched.
  */
class MaktTest extends TestForTable(makt_1) {

  lazy val tdm = TestDataManager("makt", "MaktTest.xlsx", ResourceLoader.withClass(makt_1.getClass))

  lazy val spark: SparkSession = SparkSession.active

  // Resolve the workbook relative to this test class's package, i.e.
  // src/test/resources/ct/dna/lakehouse/dm_md/fin_hawk/MaktTest.xlsx — co-located with the production class.

  override protected def beforeAll(): Unit = {
    super.beforeAll()

    tdm.dropTargetIfExists(makt_1)
    makt_1.sourceTableSpecs.foreach(tdm.dropSourceIfExists)

    tdm.createAsTarget(makt_1)
    makt_1.sourceTableSpecs.foreach(tdm.createAsSource)
  }

  override protected def afterAll(): Unit = try {
    tdm.dropTargetIfExists(makt_1)
    makt_1.sourceTableSpecs.foreach(tdm.dropSourceIfExists)
  } finally super.afterAll()

  behavior of "makt"

  it should "validateToRoot" in {
    makt_1.validateToRoot()
  }
  // ---------------------------------------------------------------------------
  // Phase 0 — empty everywhere.
  // ---------------------------------------------------------------------------

  it should "Phase 0: leave the target empty when every source is empty" in {
    tdm.update(makt_1)
    tdm.assertTargetMatches(makt_1, "target_phase0")
  }

  // ---------------------------------------------------------------------------
  // Phase 1 — initial data in two of the thirteen sources.
  // ---------------------------------------------------------------------------

  it should "Phase 1: populate target from ct_gbl_e32 and ct_gbl_epp initial feeds" in {
    tdm.syncToSource(ct_gbl_e32.makt, "e32_phase1")
    tdm.syncToSource(ct_gbl_epp.makt, "epp_phase1")

    tdm.update(makt_1)

    tdm.assertTargetMatches(makt_1, "target_phase1")
  }

  // ---------------------------------------------------------------------------
  // Phase 2 — full case matrix on ct_gbl_e32 (the EPP rows must stay intact).
  // ---------------------------------------------------------------------------

  it should "Phase 2: apply CDF inserts/updates/deletes from ct_gbl_e32 without touching EPP rows" in {
    // syncToSource MERGE-aligns the source Delta table to this sheet; the resulting CDF entries
    // (insert / update / delete per row) drive the production merge branches end-to-end.
    tdm.syncToSource(ct_gbl_e32.makt, "e32_phase2")

    tdm.update(makt_1)

    tdm.assertTargetMatches(makt_1, "target_phase2")
  }

  // ---------------------------------------------------------------------------
  // Phase 3 — snapshot semantics for ct_gbl_epp.
  // ---------------------------------------------------------------------------

  it should "Phase 3: snapshot-rebuild ct_gbl_epp deletes absent EPP rows and leaves E32 rows untouched" in {
    // Drop & re-create the EPP source so the previously-recorded known commit is no longer part of
    // its history. The framework's SourceStateReader then returns from=None, which flips
    // ChangeFeedImpl.isSnapshot to true on the next update.
    tdm.dropSourceIfExists(ct_gbl_epp.makt)
    tdm.createAsSource(ct_gbl_epp.makt)
    tdm.syncToSource(ct_gbl_epp.makt, "epp_phase3")

    tdm.update(makt_1)

    tdm.assertTargetMatches(makt_1, "target_phase3")
  }
}
